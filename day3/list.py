list=["apple","mango","orange"]
list.append("banana") #adds element at the end of the list
list.insert(1,"banana")#adds element at a particular index
list.remove("banana")#remove the first specified element{if we have two same objects}
list.reverse()#it reverse the entire list
list.sort()#it is going to sort according to the alphabetical order
list.pop(0)#it removes an elemnet from the list{if we dont specify the index value then it removes last element by default}
print(list)